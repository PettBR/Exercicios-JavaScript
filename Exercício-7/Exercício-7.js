alert("Exercício 7: Programa para conversão de Fahrenheit para Celsius")
let temperatura_fahrenheit = Number(prompt("Insira o valor númerico da temperatura em Fahrenheit."))
let temperatura_celcius = ((5/9) * (temperatura_fahrenheit - 32))

